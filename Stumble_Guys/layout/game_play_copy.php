                            <h2>How to play Stumble Guys</h2>
                            <p>When you enter the game, you will see many guys with different colors. They have the
                                same goal as you. It's racing to the end. Up to 32 players will compete in the game.
                                Each game consists of various levels. To play the
                                game, you have to control your character with the arrow keys. Run, move, and jump to
                                pass through different obstacles and try to reach the finish line as fast as you
                                can. After each round, a certain number of players
                                are qualified to progress to the next round, and others will be eliminated. The game
                                gets more difficult as you progress. And that means there will be more challenges.
                                And the last one to remain is the winner.</p>
